class AdvisoryGenerator:
    def __init__(self):
        pass
        
    def generate_advisory(self, predicted_aqi: float, attribution: dict, lang: str = "en"):
        # Normalise language input
        lang = lang.lower()
        if lang not in ADVISORIES:
            lang = "en"
            
        # Determine AQI category
        if predicted_aqi <= 50:
            cat = "Good"
        elif predicted_aqi <= 100:
            cat = "Satisfactory"
        elif predicted_aqi <= 200:
            cat = "Moderate"
        elif predicted_aqi <= 300:
            cat = "Poor"
        elif predicted_aqi <= 400:
            cat = "Very Poor"
        else:
            cat = "Severe"
            
        # Find primary source
        primary_source = "background"
        max_val = 0.0
        for src, val in attribution.items():
            if src != "background" and val > max_val:
                max_val = val
                primary_source = src
                
        # Get language pack
        lang_pack = ADVISORIES[lang]
        cat_pack = lang_pack["categories"][cat]
        
        # Build response
        return {
            "language": lang,
            "title": lang_pack["title"],
            "aqi": round(predicted_aqi),
            "category": cat,
            "general_advisory": cat_pack["general"],
            "vulnerable_advisory": cat_pack["vulnerable"],
            "primary_source": primary_source,
            "source_advisory": lang_pack["source_mapping"][primary_source]
        }

